# Define o valor limite do loop
INDICE = 13

# Inicializa a variável para armazenar a soma
SOMA = 0

# Inicializa o contador do loop
K = 0

# Loop que executa enquanto K for menor que INDICE
while K < INDICE:
    # Incrementa o contador
    K = K + 1
    # Adiciona o valor de K à soma
    SOMA = SOMA + K

# Imprime o resultado da soma
print(SOMA)